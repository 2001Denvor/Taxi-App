run_examples:-
	A is 2 + 3 * 4,
	B is (2 + 3) * 4,
	C is 20 - 6 / 2,
	D is (20 - 6) / 2,
	E is 10 - 4 - 2,

write('A = 2 + 3 * 4 		->'),write(A),nl,
write('B = (2 + 3) * 4 		->'),write(B),nl,
write('C = 20 - 6 / 2 		->'),write(C),nl,
write('D = (20 - 6) / 2		->'),write(D),nl,
write('E = 10 - 4 - 2		->'),write(E),nl.
